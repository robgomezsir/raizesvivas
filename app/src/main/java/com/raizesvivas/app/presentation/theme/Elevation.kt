package com.raizesvivas.app.presentation.theme

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Elevações padronizadas para Material Design 3
 * 
 * Usa valores adequados para criar hierarquia visual clara
 */
object RaizesElevation {
    /** Elevação para cards secundários e elementos de baixa prioridade */
    val cardSecondary: Dp = 0.dp
    
    /** Elevação padrão para cards importantes */
    val cardDefault: Dp = 0.dp
    
    /** Elevação para cards de alta prioridade (Família Zero, destaques) */
    val cardElevated: Dp = 0.dp
    
    /** Elevação para FAB (Floating Action Button) */
    val fab: Dp = 0.dp
    
    /** Elevação para modais e dialogs */
    val modal: Dp = 0.dp
    
    /** Elevação para bottom sheets */
    val bottomSheet: Dp = 0.dp
    
    /** Elevação para app bar */
    val appBar: Dp = 0.dp
}
